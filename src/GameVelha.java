    public void setarMatrizVazia(){
        for (int y = 0; y < 3; y++) {
            for (int x = 0; x < 3; x++) {
                posicoes[y][x] = "";
            }
        }
    }
    
    public void atualizarMatriz(){
        posicoes[0][0] = btn00.getText();
        posicoes[0][1] = btn01.getText();
        posicoes[0][2] = btn02.getText();
        posicoes[1][0] = btn10.getText();
        posicoes[1][1] = btn11.getText();
        posicoes[1][2] = btn12.getText();
        posicoes[2][0] = btn20.getText();
        posicoes[2][1] = btn21.getText();
        posicoes[2][2] = btn22.getText();
        contadorJogadas++;
    }
    
    public void testarVencedor(){
        for (int i = 0; i < 3; i++) {
            if ((posicoes[i][0] == posicoes[i][1] && posicoes[i][1] == posicoes[i][2]) && posicoes[i][i] != "") {
                mostrarVencedor(true);
                reset();
            }else if((posicoes[0][i] == posicoes[1][i] && posicoes[1][i] == posicoes[2][i]) && posicoes[i][i] != ""){
                mostrarVencedor(true);
                reset();
            }else if((posicoes[0][0] == posicoes[1][1] && posicoes[1][1] == posicoes[2][2]) && posicoes[1][1] != ""){
                mostrarVencedor(true);
                reset();
            }else if((posicoes[0][2] == posicoes[1][1] && posicoes[1][1] == posicoes[2][0]) && posicoes[1][1] != ""){
                mostrarVencedor(true);
                reset();
            }else if(contadorJogadas == 9){
                JOptionPane.showMessageDialog(null,"Deu Velha!");
                reset();
        }
        }
        
        
    }
    
    
    
    public void reset(){
        desabilitarBotoes();
        mostrarBotoesQuemComeca();
        setarMatrizVazia();
        jtQuemComeca.setText("");
        contadorJogadas = 0;
        painelQuemComeca.requestFocus();
        
        
    }
    
    public void mostrarVencedor(boolean v){
        
        String vencedor;
        if(jtQuemComeca.getText().equals("O")){
            vencedor = "X";
        }else{
            vencedor = "O";
        }
        
        
        if(v == true){     
            jtQuemComeca.setText("");
            JOptionPane.showMessageDialog(null, "O jogador " + vencedor + " Venceu!");
        }else;   
    }
    
    public String atualizarJogador(int y, int x){
        String jogador;
        if(contador % 2 == 0){     
            jogador = "O";
        }else{      
            jogador = "X";
        }
        contador++;
        attJogada();
        return jogador;
    }
    
    public void attJogada(){
        if(jtQuemComeca.getText().equals("X")){
            jtQuemComeca.setText("O");
        }else{
            jtQuemComeca.setText("X");
        }
    }
    
