    public void desmarcarBotoesQuemComeca(){
       qcX.setSelected(false);
       qcO.setSelected(false);
    }
    
    public void esconderBotoesQuemComeca(){
        desmarcarBotoesQuemComeca();
        painelQuemComeca.setVisible(false);
    }
    
    public void mostrarBotoesQuemComeca(){
        desmarcarBotoesQuemComeca();
        painelQuemComeca.setVisible(true);
    }
    
    
    
    public void escolherQuemComeca(){
        if(qcO.isSelected()){
            contador = 0;
        }else if (qcX.isSelected()){
            contador = 1;
        }else{
            JOptionPane.showMessageDialog(null, "algo deu errado.");
        }
    }
    public void mostrarQuemComeca(){
        if(contador % 2 == 0){
            jtQuemComeca.setText("O");
        }
        else{
            jtQuemComeca.setText("X");
        }
    }
   
